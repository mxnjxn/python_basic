# 3-3 숫자 처리 함수
from math import *  # math library 에 있는 모든 module을 사용하겠다는 의미

print("3-3 숫자 처리 함수")
# abs()
# max() , min()
# round()
# pow()

# abs --> absolute value
print(abs(-5))
print(abs(-9))
# pow() --> exponential function
print(pow(4, 2))  # 4^2
print(pow(4, 5))

# max() --> compare the numbers and output the bigger number
print(max(5, 12))
print(max(4, 6))
print(max(4, 5))
# min()
print(min(3, 2))
print(min(37, 23))
print(min(34, 2))

# round()
print(round(4.99))
print(round(2.33))

# Use modules in library math
# floor()
# ceil()
# sqrt()

print(floor(3.9))
print(floor(4.99))  # 내림.
print(ceil(3.14))  # 올림
print(sqrt(16))  # 제곱근
