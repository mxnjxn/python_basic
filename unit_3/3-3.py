# 3-3 숫자 처리 함수
from math import *  # math library 에 있는 모든 module을 사용하겠다는 의미

print("3-3 숫자 처리 함수")

# abs --> absolute value
print(abs(-5))

# pow() --> exponential function
print(pow(4, 2))  # 4^2

# max() --> compare the numbers and output the bigger number
print(max(5, 12))
print(max(4, 6))
# min()
print(min(3, 2))
print(min(37, 23))

# round()
print(round(4.99))

# Use modules in library math
print(floor(4.99))  # 내림.
print(ceil(3.14))  # 올림
print(sqrt(16))  # 제곱근
