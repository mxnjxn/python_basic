# 문자열 --> 문자들의 집합
# 여러줄로 작성할때는 """ """ --> 큰 따옴표 세개로 작성
# 큰 따옴표 안에 작은 따옴표, 작은 따옴표 안에 큰 따옴표 작성 가능

sentence = """Hello
my name is
Tony"""

print(sentence)

sentence2 = "'Hello'"
print(sentence2)

# 큰 따옴표안에 큰 따옴표를 쓰고 싶으면 "\" 역슬래시 후 " 출력
sentence3 = "\"Hello, my name is my god\""
print(sentence3)

# 주석은 웬만하면 작은 따옴표
